    args.output_stride = 8
    if args.gpu_num > 1:
        BatchNorm = SynchronizedBatchNorm2d
    else:
        BatchNorm = nn.BatchNorm2d
    model = ResNet(Bottleneck, [3, 4, 6, 3], args.output_stride, BatchNorm, in_channel=in_channel)
    return model
    